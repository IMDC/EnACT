namespace LibEnACT
{
    /// <summary>
    /// The Intensity enum represents how intense the emotion in a caption will be.
    /// </summary>
    public enum Intensity
    {
        /// <summary>
        /// Represents no intensity. Not normally used.
        /// </summary>
        None = 0,

        /// <summary>
        /// Represents low emotion intensity.
        /// </summary>
        Low = 1,

        /// <summary>
        /// Represents medium emotion intensity.
        /// </summary>
        Medium = 2,

        /// <summary>
        /// Represents high emotion intensity.
        /// </summary>
        High = 3,
    };
}
