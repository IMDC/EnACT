using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace EnACT
{
    /// <summary>
    /// The Alignment enum represents the text alignment of a caption.
    /// </summary>
    public enum Alignment
    {
        /// <summary>
        /// Represents Left text alignment.
        /// </summary>
        Left = 0,

        /// <summary>
        /// Represents Center text alignment.
        /// </summary>
        Center = 1,

        /// <summary>
        /// Represents Right text alignment.
        /// </summary>
        Right = 2,
    }
}
